#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	signe;
	int	rslt;

	i = 0;
	signe = 1;
	rslt = 0;
	while ((nptr[i] >= 9 && nptr[i] <= 13) || nptr[i] == 32)
		i++;
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			signe *= -1;
		i++;
	}
	while (nptr[i] >= 48 && nptr[i] <= 57)
	{
		rslt = rslt * 10 + nptr[i] - 48;
		i++;
	}
	return (rslt * signe);
}
