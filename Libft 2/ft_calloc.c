#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*s;

	s = malloc(nmemb * size);
	if (!s)
	{
		return (NULL);
	}
	ft_bzero(s, nmemb * size);
	return (s);
}
